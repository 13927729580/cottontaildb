package org.vitrivr.cottontail.database.queries.planning.nodes.physical.projection

import org.vitrivr.cottontail.database.column.ColumnDef
import org.vitrivr.cottontail.database.queries.OperatorNode
import org.vitrivr.cottontail.database.queries.QueryContext
import org.vitrivr.cottontail.database.queries.planning.cost.Cost
import org.vitrivr.cottontail.database.queries.planning.nodes.physical.UnaryPhysicalOperatorNode
import org.vitrivr.cottontail.database.queries.projection.Projection
import org.vitrivr.cottontail.execution.TransactionContext
import org.vitrivr.cottontail.execution.operators.projection.ExistsProjectionOperator
import org.vitrivr.cottontail.model.basics.Name
import org.vitrivr.cottontail.model.basics.Type

/**
 * A [UnaryPhysicalOperatorNode] that represents a projection operation involving aggregate functions such as [Projection.EXISTS].
 *
 * @author Ralph Gasser
 * @version 1.0.0
 */
class ExistsProjectionPhysicalOperatorNode(input: OperatorNode.Physical, val fields: List<Pair<Name.ColumnName, Name.ColumnName?>>) : UnaryPhysicalOperatorNode(input) {

    /** The [Projection] represented by this [ExistsProjectionPhysicalOperatorNode]. */
    val type: Projection = Projection.EXISTS

    /** The [ColumnDef] generated by this [ExistsProjectionPhysicalOperatorNode]. */
    override val columns: Array<ColumnDef<*>>

    /** The [ColumnDef] required by this [ExistsProjectionPhysicalOperatorNode]. */
    override val requires: Array<ColumnDef<*>> = emptyArray()

    /** The output size of this [ExistsProjectionPhysicalOperatorNode] is always one. */
    override val outputSize: Long = 1

    /** The [Cost] of a [ExistsProjectionPhysicalOperatorNode]. */
    override val cost: Cost = Cost(cpu = Cost.COST_MEMORY_ACCESS)

    /**The [ExistsProjectionPhysicalOperatorNode] cannot be partitioned. */
    override val canBePartitioned: Boolean = false

    init {
        /* Calculate necessary fields. */
        val alias = fields.first().second
        val name = alias ?: (input.columns.first().name.entity()?.column(Projection.EXISTS.label()) ?: Name.ColumnName(Projection.EXISTS.label()))
        this.columns = arrayOf(ColumnDef(name, Type.Boolean, false))
    }

    /**
     * Returns a copy of this [ExistsProjectionPhysicalOperatorNode] and its input.
     *
     * @return Copy of this [ExistsProjectionPhysicalOperatorNode] and its input.
     */
    override fun copyWithInputs() = ExistsProjectionPhysicalOperatorNode(this.input.copyWithInputs(), this.fields)

    /**
     * Returns a copy of this [ExistsProjectionPhysicalOperatorNode] and its output.
     *
     * @param input The [OperatorNode] that should act as inputs.
     * @return Copy of this [ExistsProjectionPhysicalOperatorNode] and its output.
     */
    override fun copyWithOutput(input: OperatorNode.Physical?): OperatorNode.Physical {
        require(input != null) { "Input is required for copyWithOutput() on unary physical operator node." }
        val projection = ExistsProjectionPhysicalOperatorNode(input, this.fields)
        return (this.output?.copyWithOutput(projection) ?: projection)
    }

    /**
     * Partitions this [ExistsProjectionPhysicalOperatorNode].
     *
     * @param p The number of partitions to create.
     * @return List of [OperatorNode.Physical], each representing a partition of the original tree.
     */
    override fun partition(p: Int): List<Physical> {
        throw UnsupportedOperationException("ExistsProjectionPhysicalOperatorNode cannot be partitioned.")
    }

    /**
     * Converts this [ExistsProjectionPhysicalOperatorNode] to a [ExistsProjectionOperator].
     *
     * @param tx The [TransactionContext] used for execution.
     * @param ctx The [QueryContext] used for the conversion (e.g. late binding).
     */
    override fun toOperator(tx: TransactionContext, ctx: QueryContext) = ExistsProjectionOperator(this.input.toOperator(tx, ctx))

    /**
     * Compares this [ExistsProjectionPhysicalOperatorNode] to another object.
     *
     * @param other The other [Any] to compare this [ExistsProjectionPhysicalOperatorNode] to.
     * @return True if other equals this, false otherwise.
     */
    override fun equals(other: Any?): Boolean {
        if (this === other) return true
        if (other !is ExistsProjectionPhysicalOperatorNode) return false
        if (type != other.type) return false
        if (fields != other.fields) return false
        return true
    }

    /**
     * Generates and returns a hash code for this [ExistsProjectionPhysicalOperatorNode].
     */
    override fun hashCode(): Int {
        var result = type.hashCode()
        result = 31 * result + fields.hashCode()
        return result
    }

    /** Generates and returns a [String] representation of this [ExistsProjectionPhysicalOperatorNode]. */
    override fun toString() = "${this.groupId}:Exists[${this.columns.joinToString(",") { it.name.toString() }}]"
}